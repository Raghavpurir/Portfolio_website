function submitForm() {
    // Get form input values
    var name = document.querySelector('.contact__input[name="name"]').value;
    var email = document.querySelector('.contact__input[name="email"]').value;
    var linkedin = document.querySelector('.contact__input[name="linkedin"]').value;
    var message = document.querySelector('.contact__input[name="message"]').value;

    // Simple form validation
    if (!name || !email || !linkedin || !message) {
        alert("Please fill in all fields.");
        return;
    }

    // Construct the data to be sent to the server
    var formData = {
        name: name,
        email: email,
        linkedin: linkedin,
        message: message
    };

    // Assuming you are using fetch API for simplicity (you might need to adjust this based on your server)
    fetch('path-to-your-server-script', {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json'
        },
        body: JSON.stringify(formData)
    })
    .then(response => response.json())
    .then(data => {
        // Handle the response from the server
        document.getElementById('submission-status').innerText = data.message;
    })
    .catch(error => {
        console.error('Error:', error);
    });
}


/*===== MENU SHOW =====*/ 
const showMenu = (toggleId, navId) =>{
    const toggle = document.getElementById(toggleId),
    nav = document.getElementById(navId)

    if(toggle && nav){
        toggle.addEventListener('click', ()=>{
            nav.classList.toggle('show')
        })
    }
}
showMenu('nav-toggle','nav-menu')

/*==================== REMOVE MENU MOBILE ====================*/
const navLink = document.querySelectorAll('.nav__link')

function linkAction(){
    const navMenu = document.getElementById('nav-menu')
    // When we click on each nav__link, we remove the show-menu class
    navMenu.classList.remove('show')
}
navLink.forEach(n => n.addEventListener('click', linkAction))

/*==================== SCROLL SECTIONS ACTIVE LINK ====================*/
const sections = document.querySelectorAll('section[id]')

function scrollActive(){
    const scrollY = window.pageYOffset

    sections.forEach(current =>{
        const sectionHeight = current.offsetHeight
        const sectionTop = current.offsetTop - 50;
        sectionId = current.getAttribute('id')

        if(scrollY > sectionTop && scrollY <= sectionTop + sectionHeight){
            document.querySelector('.nav__menu a[href*=' + sectionId + ']').classList.add('active')
        }else{
            document.querySelector('.nav__menu a[href*=' + sectionId + ']').classList.remove('active')
        }
    })
}
window.addEventListener('scroll', scrollActive)

